class MeetingLink(Base):
    __tablename__ = "meeting_links"

    id: Mapped[str] = mapped_column(String, primary_key=True)
    source_meeting_id: Mapped[str] = mapped_column(ForeignKey("meetings.id", ondelete="CASCADE"), index=True)
    target_meeting_id: Mapped[str] = mapped_column(ForeignKey("meetings.id", ondelete="CASCADE"), index=True)
