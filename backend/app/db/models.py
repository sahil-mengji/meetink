from datetime import datetime
from typing import Optional

from sqlalchemy import String, DateTime, ForeignKey
from sqlalchemy.orm import Mapped, mapped_column, relationship
from sqlalchemy.dialects.postgresql import JSONB, ARRAY
from pgvector.sqlalchemy import Vector

from app.db.database import Base


class User(Base):
    __tablename__ = "users"

    id: Mapped[str] = mapped_column(String, primary_key=True)
    name: Mapped[str] = mapped_column(String, nullable=False)
    email: Mapped[Optional[str]] = mapped_column(String)


class VoiceProfile(Base):
    __tablename__ = "voice_profiles"

    id: Mapped[str] = mapped_column(String, primary_key=True)
    user_id: Mapped[str] = mapped_column(ForeignKey("users.id", ondelete="CASCADE"))
    name: Mapped[str] = mapped_column(String, nullable=False)
    embedding = mapped_column(Vector(192))  # SpeechBrain ECAPA-TDNN 192-dimensional vector

    user: Mapped["User"] = relationship()


class MeetingSpeakerMapping(Base):
    __tablename__ = "meeting_speaker_mappings"

    meeting_id: Mapped[str] = mapped_column(ForeignKey("meetings.id", ondelete="CASCADE"), primary_key=True)
    raw_speaker_name: Mapped[str] = mapped_column(String, primary_key=True)
    user_id: Mapped[str] = mapped_column(ForeignKey("users.id", ondelete="CASCADE"))

    meeting: Mapped["Meeting"] = relationship()
    user: Mapped["User"] = relationship()


class Meeting(Base):
    __tablename__ = "meetings"

    id: Mapped[str] = mapped_column(String, primary_key=True)
    title: Mapped[str] = mapped_column(String, nullable=False)
    recorded_at: Mapped[Optional[datetime]] = mapped_column(DateTime(timezone=True))
    participants: Mapped[list[str]] = mapped_column(ARRAY(String), default=list)
    
    # URL to the original raw .vtt file in AWS S3 or Google Cloud Storage
    raw_vtt_url: Mapped[Optional[str]] = mapped_column(String)
    raw_vtt_content: Mapped[Optional[str]] = mapped_column(String)
    audio_filepath: Mapped[Optional[str]] = mapped_column(String)
    audio_url: Mapped[Optional[str]] = mapped_column(String)
    status: Mapped[str] = mapped_column(String, default="Processing")
    duration: Mapped[str] = mapped_column(String, default="45m")
    
    # Relationships
    transcript: Mapped["MeetingTranscript"] = relationship(back_populates="meeting", uselist=False, cascade="all, delete-orphan")
    highlights: Mapped[list["MeetingHighlight"]] = relationship(back_populates="meeting", cascade="all, delete-orphan")


class MeetingTranscript(Base):
    __tablename__ = "meeting_transcripts"

    meeting_id: Mapped[str] = mapped_column(ForeignKey("meetings.id", ondelete="CASCADE"), primary_key=True)
    
    # This stores the array of dicts: [{"id": "msg_1", "speaker": "Anish", "text": "...", "start": 1.2, "end": 4.5}]
    cleaned_transcript: Mapped[list[dict]] = mapped_column(JSONB, default=list)
    
    # Relationship
    meeting: Mapped["Meeting"] = relationship(back_populates="transcript")


class MeetingHighlight(Base):
    __tablename__ = "meeting_highlights"

    id: Mapped[str] = mapped_column(String, primary_key=True)
    meeting_id: Mapped[str] = mapped_column(ForeignKey("meetings.id", ondelete="CASCADE"))
    
    # "action_item", "decision", "risk", "proposal", etc.
    highlight_type: Mapped[str] = mapped_column(String, index=True)
    
    # The condensed sentence generated by the LLM
    text: Mapped[str] = mapped_column(String)
    
    # If the LLM identifies an owner (for action items)
    owner: Mapped[Optional[str]] = mapped_column(String)
    
    # Traceability: The utterance IDs from the cleaned_transcript
    source_ids: Mapped[list[str]] = mapped_column(ARRAY(String), default=list)

    # Relationship
    meeting: Mapped["Meeting"] = relationship(back_populates="highlights")


