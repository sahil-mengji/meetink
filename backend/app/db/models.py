class KnowledgeGroup(Base):
    """One row per unique tag. Updated by the pipeline after each meeting is processed.
    Stores LLM-synthesized group-level metadata: title, description, goal, and a progress gist.
    """
    __tablename__ = "knowledge_groups"

    id: Mapped[str] = mapped_column(String, primary_key=True)  # slugified tag name
    tag: Mapped[str] = mapped_column(String, unique=True, index=True)
    group_title: Mapped[Optional[str]] = mapped_column(String)
    group_description: Mapped[Optional[str]] = mapped_column(String)
    group_goal: Mapped[Optional[str]] = mapped_column(String)
    progress_gist: Mapped[Optional[str]] = mapped_column(String)
    meeting_count: Mapped[int] = mapped_column(default=0)
    updated_at: Mapped[Optional[datetime]] = mapped_column(DateTime(timezone=True), default=datetime.utcnow)


